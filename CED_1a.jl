## Dynamical Systems
"""

# ╔═╡ 706cd06f-d1ce-4990-beb9-b7ff45dfcc85
md"""
Let 

$xₜ ∈ \mathbb{R}^n$ be a vector describing the system or the **State Variable**.

A **Dynamical System** is a map from a states to new states:

$x_{t+1} = f_λ(x_t)$

where:

$f:\mathbb{R}^n \to \mathbb{R}^n$ is the evolution law or **law of motion** and

$λ ∈ \mathbb{R}^m$ is a set of parameters influencing the system.

"""

# ╔═╡ cb6cd264-06f6-49da-b5e0-b242e31ffb41
md"""
>Our aim is to understand the long run behavior of the states and its dependence on parameters and the initial state x₀.
"""

# ╔═╡ 0723f19c-7ea5-4e8f-a8f9-06c35e29d982
md"""
### Some examples:
"""

# ╔═╡ 23b773a3-1e38-4db7-bb6c-6473e745f17d
md"""
#### Quadratic map:

$x_{t+1} = λx_t(1-x_t)$


"""

# ╔═╡ 0db701b5-871a-4bfd-b822-571fc5178fca
md"""
#### Linear Dynamical System:

$x_{t+1} = λx_t$

which is a naive case as we can actually derive an explicit solution in:

$x_t = λ^t x_0$


"""

# ╔═╡ ba09610a-568a-4ecc-8c86-4e3aad045639
md"""
#### Hénon Map:

$x_{t+1} = 1 - ax_t + y_t$
$y_{t+1} = bx_t$



"""

# ╔═╡ c19fdb3e-34f4-408d-9012-2ca223710ac3
md"""
!!! correct "Remarks on notation"
	An orbit is a sequence of states: $\{x_0, x_1, x_2, \dots\}$

	where: 
	
	$x_1 = f(x_0)$

	$x_2 = f(x_1) = f(f(x_0)) = (f ∘ f) x_0$
	
	$x_3 = f(x_2) = f(f(f(x_0))) = (f ∘ f ∘ f) x_0$

	In general we have:
	
	$x_n = f(x_{n-1}) = (f ∘ f ∘ \dots∘ f) x_0 = f^n(x_0)$

	Note: "∘" reads "follow".
"""

# ╔═╡ 67553a74-0123-436c-b0e3-fbb210586e01
md"""
### Steady States

A point $x^*$ is a **Steady State** or a **Fixed point** of the evolution $f$ if:

$x^* = f(x^*)$
"""

# ╔═╡ b2a1ff72-7df6-4947-869c-17f4ea925cac
md"""
In a one dimensional setting, the fixed points are graphically represented by the intersection of the map with the line $y = x$.
For example if we consider the quadratic map:

$f_4(x) = 4x(1-x)$
we have:
"""

