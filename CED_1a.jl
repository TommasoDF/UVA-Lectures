# ╔═╡ 1bd7135b-1f3b-424c-a078-c9f93f8413de
md"""
### Local Stability
"""

# ╔═╡ f0667de6-49f3-472f-9f23-cca8cbfef933
md"""
!!! danger "Error in the book"
	A fixed point $x^*$ is locally stable if there is an $ϵ>0$ s.t. $∀ x_0 ∈ (x^* - ϵ, x^* + ϵ)$ we have: 

	$\lim_{t \to ∞} x_t = \lim_{t \to ∞} f^t(x_0) = x^*$

But this is wrong!

"""

# ╔═╡ 88de993a-21ef-48de-a4b3-9a129210f04e
md"""
!!! correct "Local Stability (actual definition)"
	A Steady State $x^*$ of a dynamical system is locally (asymptotically) stable if $∀ δ >0 ∃ ϵ>0$ s.t. $∀ x_0 ∈ (x^* - ϵ, x^* + ϵ)$ we have that: 

	 $x_t = f^t(x_0) ∈ (x^* - δ, x^* + δ)$ and $x_t = f^t(x_0) → x^*$ as $x → ∞$



"""

# ╔═╡ 547517c0-e434-46f1-8a3e-858882948c58
md"""
!!! correct "Unstable Steady State"
	Conversely we can state that a Steady State $x^*$ of a dynamical system is **unstable** if $∃ δ >0$ s.t. $∀ϵ>0 \quad ∃ x_0 ∈ (x^* - ϵ, x^* + ϵ)$ such that we have: 

	 $x_t = f^t(x_0) ∉ (x^* - δ, x^* + δ)$ 



"""

# ╔═╡ 2da97841-aa53-4453-923a-6b6b57ba5a2e
md"""
>**Local Stability Theorem**
>
> if $f:\mathbb{R} → \mathbb{R}$ is continously differentiable and $x^*$ is a fixed 								point of $f$, then if:
>
>$|f'(x^*)| < 1 ⟹ x^* \quad \text{is locally stable}$
>
>$|f'(x^*)| > 1 ⟹ x^*\quad \text{is unstable}$ 
"""

# ╔═╡ d3b22afb-7769-467b-9ccc-2c5b9c042950
md"""
Below we can find examples of:
- (a) Monotonic convergence, when $\quad f'(x^*) < 1$
- (b) Monotonic divergence, when $\quad f'(x^*) >1$
- (c) Oscillatory convergence, when $\quad -1 < f'(x^*) <0$
- (d) Oscillatory divergence, when $\quad f'(x^*) < -1$

"""

