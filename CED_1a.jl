# ╔═╡ fc371e15-4618-4ac6-8498-09613f76e02b
md"""
#### Periodic Points

A point x is a **periodic point** of period k if:

$f^k(x) = x \quad \text{and} \quad f^t(x)≠x \quad 0 < t < k$

A **periodic orbit** or (**k-cycle**) can be defined as $\{x, f(x), \dots , f^{k-1}(x)\}$

**Note:** `a periodic $k$ point of $f$ is a fixed point of $f^k$`

"""

# ╔═╡ 192ae405-2352-4b92-b27f-5d1556ebf0be
md"""
>**Local Stability for period k points**
>
>$|f^k(x)| < 1 ⟹  \quad \text{period k point x is locally stable}$
>
>$|f^k(x)| > 1 ⟹ \quad \text{period k point x is unstable}$ 
"""

# ╔═╡ 4ec6dd5b-e25b-4181-b710-4734ad423bf0
md"""
#### Aperiodic Points

A point is **aperiodic** if:

- Its orbit is bounded;
- It is not a periodic point
- Its orbit does not converge to a periodic orbit


"""

# ╔═╡ 97137176-dc20-454a-a971-2cdf99c185cf
function cobweb(g::Function,x0::Float64,
maxit::Int)
	r =  -0:0.01:1.5
	p = plot(g,r,xlims = (0.0,1.5), ylims = (0,1.5))
	diagonal(x) = x
	plot!(diagonal, r, label="")	
	x = x0
	
	for i=1:maxit
		y = g(x)
		
		plot!([x,y],[y,y], line=( :black), label="")
		plot!([x,x], [x,y],line=( :black), label="")
		x = y
	end
	p
end
	

